int	ft_isfloat(char *s)
{
	int	i;
	int	dot;

	i = 0;
	dot = 0;
	if (s[0] == '-' || s[0] == '+')
		i++;
	while (s[i] && ((s[i] >= '0' && s[i] <= '9') || (!dot && s[i] == '.')))
	{
		dot = dot || s[i] == '.';
		i++;
	}
	return (!s[i]);
}
