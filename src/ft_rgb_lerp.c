#include "libft.h"

t_rgb	ft_rgb_lerp(t_rgb c1, t_rgb c2, float t)
{
	return ((t_rgb){c1.r * (1.0f - t) + c2.r * t,
		c1.g * (1.0f - t) + c2.g * t,
		c1.b * (1.0f - t) + c2.b * t});
}
